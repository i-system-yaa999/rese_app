class RandJP{
  public function rand_hiragana($length)
  {
    static $chars = array(
      'ぁ', 'ぃ', 'ぅ', 'ぇ', 'ぉ',
      'あ', 'い', 'う', 'え', 'お',
      'か', 'き', 'く', 'け', 'こ',

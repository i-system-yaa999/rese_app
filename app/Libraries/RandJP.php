      'だ', 'ぢ', 'づ', 'で', 'ど',
      'な', 'に', 'ぬ', 'ね', 'の',
      'は', 'ひ', 'ふ', 'へ', 'ほ',
      'ば', 'び', 'ぶ', 'べ', 'ぼ',
      'ぱ', 'ぴ', 'ぷ', 'ぺ', 'ぽ',
      'ま', 'み', 'む', 'め', 'も',
      'ゃ', 'ゅ', 'ょ',
      'や', 'ゆ', 'よ',
      'ゎ',
      'わ', 'を', 'ん',
    );
    $str = '';
    for ($i = 0; $i < $length; ++$i) {
      $str .= $chars[array_rand($chars)];
    }
    return $str;
